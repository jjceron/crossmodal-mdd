# ── Model wrappers ──────────────────────────────────────────────────────

class ShallowConvNetWrapper(nn.Module):
    def __init__(self, n_channels, n_samples):
        super().__init__()
        self.m = ShallowConvNet(n_channels, 1, n_samples, 0.5)
    def forward(self, x): return self.m(x).squeeze(-1)


class DeepConvNetWrapper(nn.Module):
    def __init__(self, n_channels, n_samples):
        super().__init__()
        self.m = DeepConvNet(n_channels, 1, n_samples, 0.5)
    def forward(self, x): return self.m(x).squeeze(-1)


class EEGNetWrapper(nn.Module):
    def __init__(self, n_channels, n_samples=None):
        super().__init__()
        self.m = EEGNet(n_channels, 1, F1=16, D=4, F2=32,
                        temporal_kern=31, separable_kern=15,
                        pool1=4, pool2=4, dropout=0.5, meanmax_alpha=0.0)
    def forward(self, x): return self.m(x)[0].squeeze(-1)


class CNNLSTMWrapper(nn.Module):
    def __init__(self, n_channels, n_samples):
        super().__init__()
        self.m = CNNLSTM(n_channels, 1, n_samples, dropout=0.5)
    def forward(self, x): return self.m(x).squeeze(-1)


MODEL_REGISTRY = {
    'shallowconvnet': ('ShallowConvNet', ShallowConvNetWrapper),
    'deepconvnet': ('DeepConvNet', DeepConvNetWrapper),
    'eegnet': ('EEGNet', EEGNetWrapper),
    'cnnlstm': ('CNN-LSTM', CNNLSTMWrapper),
}


# ── Data loading from cache ─────────────────────────────────────────────

def load_cached_eeg(n_ch):
    """Load EEG windows from preprocessed cache. Returns (data, labels, cods, n_samples)."""
    cache_path = f'data/processed/eeg_preprocessed_{n_ch}ch.npz'
    if not os.path.exists(cache_path):
        raise FileNotFoundError(
            f'Cache not found: {cache_path}\n'
            f'  Run: py src/preprocess/cache_modma_eeg.py --channels {n_ch}')
    c = np.load(cache_path, allow_pickle=True)
    data = list(c['windows'])
    labels = c['labels'].astype(int)
    cods = list(c['subject_ids'])
    n_samples = data[0].shape[2]
    actual_n_ch = data[0].shape[1]

    n_mdd = int(labels.sum())
    n_hc = int((1 - labels).sum())
    total_wins = sum(w.shape[0] for w in data)

    print(f'  Cache: {cache_path}')
    print(f'  Subjects: {len(cods)}  MDD={n_mdd} HC={n_hc}')
    print(f'  Windows: {total_wins} total, ~{total_wins // len(cods)}/subject')
    print(f'  Shape: ({actual_n_ch}, {n_samples}) x ~{total_wins // len(cods)} windows/subject')
    assert actual_n_ch == n_ch, f'Cache channels ({actual_n_ch}) != expected ({n_ch})'
    assert labels.shape[0] == len(cods)
    return data, labels, cods, n_samples


# ── Partitioning ────────────────────────────────────────────────────────

class WindowDataset(Dataset):
    """Flatten windows per subject, normalize each window independently."""

    def __init__(self, windows_list, labels_list, subj_names, indices):
        self.X, self.y, self.subj = [], [], []
        for idx in indices:
            for w in windows_list[idx]:
                self.X.append((w - w.mean()) / (w.std() + 1e-8))
                self.y.append(labels_list[idx])
                self.subj.append(subj_names[idx])
        self.X = torch.from_numpy(np.array(self.X)).float()
        self.y = torch.tensor(self.y, dtype=torch.float)

    def __len__(self):
        return len(self.y)

    def __getitem__(self, idx):
        return self.X[idx], self.y[idx], self.subj[idx]


def create_dataloaders(subject_data, labels, subject_names,
                       k_folds=5, batch_size=32, seed=RANDOM_STATE,
                       inner_folds=5, num_workers=0, pin_memory=True):
    """Return list of (train_loader, val_loader, test_loader) tuples."""
    outer = StratifiedGroupKFold(n_splits=k_folds, shuffle=True, random_state=seed)
    folds = []
    for fi, (tvi, tei) in enumerate(outer.split(np.zeros(len(labels)), labels, groups=subject_names)):
        inner = StratifiedGroupKFold(n_splits=inner_folds, shuffle=True, random_state=seed + fi)
        tr_i, vi = next(inner.split(np.zeros(len(tvi)),
                                     [labels[i] for i in tvi],
                                     groups=[subject_names[i] for i in tvi]))
        train_ds = WindowDataset(subject_data, labels, subject_names, [tvi[i] for i in tr_i])
        val_ds = WindowDataset(subject_data, labels, subject_names, [tvi[i] for i in vi])
        test_ds = WindowDataset(subject_data, labels, subject_names, tei)
        folds.append((
            DataLoader(train_ds, batch_size, shuffle=True, num_workers=num_workers, pin_memory=pin_memory),
            DataLoader(val_ds, batch_size, shuffle=False, num_workers=num_workers, pin_memory=pin_memory),
            DataLoader(test_ds, batch_size, shuffle=False, num_workers=num_workers, pin_memory=pin_memory),
        ))
    return folds


# ── Training helpers ────────────────────────────────────────────────────

def _logits_to_binary(logits):
    return (torch.sigmoid(logits).cpu().numpy() >= 0.5).astype(int)


def _compute_epoch_metrics(model, loader, crit, logger):
    model.eval()
    total_loss, n = 0.0, 0
    all_logits, all_labels = [], []
    with torch.no_grad():
        for X, y, _ in loader:
            X, y = X.to(device), y.to(device).float()
            logits = model(X)
            total_loss += crit(logits, y).item() * X.size(0)
            n += X.size(0)
            all_logits.append(logits)
            all_labels.append(y)
    loss = total_loss / n
    preds = _logits_to_binary(torch.cat(all_logits))
    trues = torch.cat(all_labels).cpu().numpy()
    return loss, logger.metrics(trues, preds)


def train_window_fold(model, train_loader, val_loader, args, logger):
    """Train one fold, return (best_state, best_val_bacc, history)."""
    opt = torch.optim.AdamW(model.parameters(), lr=args.lr, weight_decay=args.wd, foreach=False)
    sched = torch.optim.lr_scheduler.ReduceLROnPlateau(
        opt, mode='max', factor=0.5, patience=5)
    crit = nn.BCEWithLogitsLoss()
    best_vb, best_st, pat = 0.0, None, 0
    history = {k: [] for k in ('train_loss', 'val_loss', 'train_acc', 'val_acc',
                                'val_bacc', 'val_f1', 'val_sens', 'val_spec')}

    logger.log_header()
    for ep in range(1, args.epochs + 1):
        model.train()
        tr_loss, tr_n = 0.0, 0
        tr_logits, tr_labels = [], []
        for X, y, _ in train_loader:
            X, y = X.to(device), y.to(device).float()
            opt.zero_grad()
            logits = model(X)
            if torch.isnan(logits).any():
                raise RuntimeError('NaN in logits — training diverged')
            y_smooth = y * 0.95 + 0.025
            loss = crit(logits, y_smooth)
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            opt.step()
            tr_loss += loss.item() * X.size(0)
            tr_n += X.size(0)
            tr_logits.append(logits.detach())
            tr_labels.append(y)
        tr_loss /= tr_n

        tr_pred = _logits_to_binary(torch.cat(tr_logits))
        tr_true = torch.cat(tr_labels).cpu().numpy()
        tr_m = logger.metrics(tr_true, tr_pred)

        vl_loss, vl_m = _compute_epoch_metrics(model, val_loader, crit, logger)
        sched.step(vl_m['bacc'])

        history['train_loss'].append(float(tr_loss))
        history['val_loss'].append(float(vl_loss))
        history['train_acc'].append(tr_m['acc'])
        history['val_acc'].append(vl_m['acc'])
        for k in ('bacc', 'f1', 'sens', 'spec'):
            history[f'val_{k}'].append(vl_m[k])

        if vl_m['bacc'] > best_vb:
            best_vb = vl_m['bacc']
            best_st = {k: v.cpu().clone() for k, v in model.state_dict().items()}
            best_opt = copy.deepcopy(opt.state_dict())
            pat = 0
        else:
            pat += 1

        if ep == 1 or pat == 0 or ep % 10 == 0:
            logger.log_epoch(ep, tr_loss, vl_loss, tr_m, vl_m, pat)

        if pat >= args.patience:
            break

    return best_st, best_opt, best_vb, history


def evaluate_subject_level(model, loader, threshold=0.5):
    """Aggregate window predictions per subject -> (y_true, y_pred, y_prob)."""
    model.eval()
    subj_preds, subj_true = {}, {}
    with torch.no_grad():
        for X, y, names in loader:
            probs = torch.sigmoid(model(X.to(device))).cpu().numpy()
            for i, sname in enumerate(names):
                subj_preds.setdefault(sname, []).append(probs[i])
                subj_true[sname] = y[i].item()
    subjects_sort = sorted(subj_true.keys())
    y_true = np.array([subj_true[s] for s in subjects_sort])
    y_prob = np.array([np.mean(subj_preds[s]) for s in subjects_sort])
    y_pred = (y_prob >= threshold).astype(int)
    return y_true, y_pred, y_prob


def evaluate_window_level(model, loader):
    """Predict all windows individually -> (y_true, y_pred)."""
    model.eval()
    all_y, all_pred = [], []
    with torch.no_grad():
        for X, y, _ in loader:
            probs = torch.sigmoid(model(X.to(device))).cpu().numpy()
            all_y.append(y.cpu().numpy())
            all_pred.append((probs >= 0.5).astype(int))
    return np.concatenate(all_y), np.concatenate(all_pred)


def _save_checkpoint(model_state, opt_state, model_key, n_ch, fold_num, args):
    out_dir = os.path.join('outputs/results/classical_dl/trained_eeg',
                           f'{model_key}_{n_ch}ch')
    os.makedirs(out_dir, exist_ok=True)
    torch.save({
        'model_state_dict': model_state,
        'optimizer_state_dict': opt_state,
    }, os.path.join(out_dir, f'fold_{fold_num}.pt'))
    if fold_num == 1:
        config = vars(args).copy()
        config['random_state'] = RANDOM_STATE
        with open(os.path.join(out_dir, 'config.json'), 'w') as f:
            json.dump(config, f, indent=2)


# ── Progressive save ────────────────────────────────────────────────────

def _save_partial(model_key, display_name, n_ch, args, n_subjects, n_mdd, n_hc,
                  fold_results_so_far, all_histories_so_far):
    baccs = [r['test_metrics']['bacc'] for r in fold_results_so_far]
    accs = [r['test_metrics']['acc'] for r in fold_results_so_far]
    f1s = [r['test_metrics']['f1'] for r in fold_results_so_far]
    summary = {
        'bacc_mean': float(np.mean(baccs)), 'bacc_std': float(np.std(baccs)),
        'acc_mean': float(np.mean(accs)), 'acc_std': float(np.std(accs)),
        'f1_mean': float(np.mean(f1s)), 'f1_std': float(np.std(f1s)),
    }
    results_clean = [{k: v for k, v in r.items() if k != 'history'} for r in fold_results_so_far]
    out_results = {
        'model': display_name, 'model_key': model_key, 'n_channels': n_ch,
        'n_subjects': n_subjects, 'n_mdd': n_mdd, 'n_hc': n_hc,
        'args': vars(args), 'folds': results_clean, 'summary': summary,
    }
    out_curves = {'model': display_name, 'model_key': model_key,
                  'n_channels': n_ch, 'folds': all_histories_so_far}
    out_name = f'{model_key}_{n_ch}ch'
    with open(os.path.join(OUTPUT_DIR, f'{out_name}.json'), 'w') as f:
        json.dump(out_results, f, indent=2)
    with open(os.path.join(OUTPUT_DIR, f'{out_name}_curves.json'), 'w') as f:
        json.dump(out_curves, f, indent=2)


# ── Main ────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description='MODMA EEG DL Benchmark')
    parser.add_argument('--model', required=True, choices=list(MODEL_REGISTRY.keys()))
    parser.add_argument('--channels', type=int, default=64, choices=[64, 19],
                        help='cache to load (must match pre-generated cache)')
    parser.add_argument('--batch-size', type=int, default=32)
    parser.add_argument('--lr', type=float, default=5e-4)
    parser.add_argument('--wd', type=float, default=1e-3)
    parser.add_argument('--epochs', type=int, default=100)
    parser.add_argument('--patience', type=int, default=15)
    parser.add_argument('--k-folds', type=int, default=5)
    parser.add_argument('--inner-folds', type=int, default=3)
    parser.add_argument('--save-model', action='store_true',
                        help='Save best model (.pt) and config per fold')
    args = parser.parse_args()

    display_name, model_cls = MODEL_REGISTRY[args.model]
    logger = ClassificationLogger()
    print(f'Device: {device}')
    print(f'MODMA EEG DL BENCHMARK — {display_name} ({args.channels}ch)')

    data, labels, cods, n_samples = load_cached_eeg(args.channels)
    n_ch = args.channels
    n_subjects = len(cods)
    n_mdd = int(labels.sum())
    n_hc = int((1 - labels).sum())

    folds = create_dataloaders(data, labels.tolist(), cods,
                               k_folds=args.k_folds, batch_size=args.batch_size,
                               inner_folds=args.inner_folds)
    fold_results, all_histories = [], []
    for fi, (tr_loader, val_loader, te_loader) in enumerate(folds):
        try:
            model = model_cls(n_channels=n_ch, n_samples=n_samples).to(device)
            torch.cuda.empty_cache()
            if fi == 0:
                print(f'  Params: {sum(p.numel() for p in model.parameters()):,}')
            best_st, best_opt, best_vb, history = train_window_fold(model, tr_loader, val_loader, args, logger)
            model.load_state_dict(best_st)
            if args.save_model:
                _save_checkpoint(best_st, best_opt, args.model, args.channels, fi + 1, args)
            y_true_s, y_pred_s, y_prob_s = evaluate_subject_level(model, te_loader)
            y_true_w, y_pred_w = evaluate_window_level(model, te_loader)
            cm_subject = confusion_matrix(y_true_s, y_pred_s).tolist()
            cm_window = confusion_matrix(y_true_w, y_pred_w).tolist()
            roc_auc = float(roc_auc_score(y_true_s, y_prob_s))
            roc_data = {'y_true': y_true_s.tolist(), 'y_prob': y_prob_s.tolist()}
            fm = logger.log_fold_test(y_true_s, y_pred_s)
            fold_results.append({'fold': fi + 1, 'best_val_bacc': float(best_vb),
                                  'n_epochs': len(history['train_loss']),
                                  'test_metrics': fm, 'history': history,
                                  'test_cm_subject': cm_subject,
                                  'test_cm_window': cm_window,
                                  'test_roc': roc_data, 'test_roc_auc': roc_auc})
            all_histories.append({'fold': fi + 1, 'history': history})
            print(f'  Fold {fi + 1}: bacc={fm["bacc"]:.3f}')
            _save_partial(args.model, display_name, args.channels, args,
                          n_subjects, n_mdd, n_hc, fold_results, all_histories)
        except Exception as e:
            print(f'  Fold {fi + 1} FAILED: {e}')
            import traceback
            traceback.print_exc()
        finally:
            if torch.cuda.is_available():
                torch.cuda.empty_cache()

    if fold_results:
        logger.log_summary(n_folds=args.k_folds)

        baccs = [r['test_metrics']['bacc'] for r in fold_results]
        accs = [r['test_metrics']['acc'] for r in fold_results]
        f1s = [r['test_metrics']['f1'] for r in fold_results]
        summary = {
            'bacc_mean': float(np.mean(baccs)), 'bacc_std': float(np.std(baccs)),
            'acc_mean': float(np.mean(accs)), 'acc_std': float(np.std(accs)),
            'f1_mean': float(np.mean(f1s)), 'f1_std': float(np.std(f1s)),
        }
        print(f'\n{display_name} ({args.channels}ch): bacc={summary["bacc_mean"]:.3f} +/- {summary["bacc_std"]:.3f}')

        results_clean = [{k: v for k, v in r.items() if k != 'history'} for r in fold_results]
        out_results = {
            'model': display_name, 'model_key': args.model, 'n_channels': args.channels,
            'n_subjects': n_subjects, 'n_mdd': n_mdd, 'n_hc': n_hc,
            'args': vars(args), 'folds': results_clean, 'summary': summary,
        }
        out_curves = {'model': display_name, 'model_key': args.model,
                      'n_channels': args.channels, 'folds': all_histories}
        out_name = f'{args.model}_{args.channels}ch'
        with open(os.path.join(OUTPUT_DIR, f'{out_name}.json'), 'w') as f:
            json.dump(out_results, f, indent=2)
        with open(os.path.join(OUTPUT_DIR, f'{out_name}_curves.json'), 'w') as f:
            json.dump(out_curves, f, indent=2)
        print(f'Saved: {OUTPUT_DIR}/{out_name}.json + _curves.json')
    else:
        print('\nAll folds failed — no results saved')


if __name__ == '__main__':
    main()


# ── Legacy: raw EDF loading (preserved for reference, not imported by main) ──
# The functions below were the original data-loading pathway prior to the
# unified cache. They are kept for reproducibility and cache regeneration.

def _LEGACY_load_participants():
    import pandas as pd
    _EEG_DIR = 'data/raw/modma/MODMA_EEG_BIDS_format/EEG_LZU_2015_2_resting state'
    p = pd.read_csv(f'{_EEG_DIR}/participants.tsv', sep='\t', header=None,
                    skiprows=1, on_bad_lines='skip', engine='python')
    return dict(zip(p[0], p[6]))


def _LEGACY_extract_windows(data, sfreq, n_ch=64, window_size=2.0, overlap=0.5):
    ws = int(window_size * sfreq)
    stride = int(ws * (1 - overlap))
    n_w = (data.shape[1] - ws) // stride + 1
    if n_w < 1:
        return None
    win = np.lib.stride_tricks.sliding_window_view(
        data[:n_ch], ws, axis=1)[:, ::stride].transpose(1, 0, 2)
    return win[:n_w].astype(np.float32)


def _LEGACY_load_raw_subjects(n_ch=64, random_state=42):
    import glob, mne
    _EEG_DIR = 'data/raw/modma/MODMA_EEG_BIDS_format/EEG_LZU_2015_2_resting state'
    sg = _LEGACY_load_participants()
    subjects = {}
    for sd in sorted(glob.glob(os.path.join(_EEG_DIR, 'sub-*'))):
        sid = os.path.basename(sd)
        if sg.get(sid) not in ('MDD', 'HC'):
            continue
        edf = glob.glob(os.path.join(sd, 'eeg', '*Resting-state*eeg.EDF'))
        if not edf:
            edf = glob.glob(os.path.join(sd, 'eeg', '*.EDF'))
        if not edf:
            continue
        try:
            raw = mne.io.read_raw_edf(edf[0], preload=True, verbose=False)
        except Exception:
            continue
        data = raw.get_data()
        if data.shape[0] < n_ch:
            continue
        wins = _LEGACY_extract_windows(data, int(raw.info['sfreq']), n_ch=n_ch)
        if wins is None:
            continue
        if wins.shape[0] > 200:
            idx = np.random.RandomState(random_state).choice(
                wins.shape[0], 200, replace=False)
            wins = wins[idx]
        subjects[sid] = {'windows': wins, 'label': 1 if sg[sid] == 'MDD' else 0}
    return subjects


