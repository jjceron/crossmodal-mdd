"""
MODMA EEG DL benchmark training script.

Models:
  shallowconvnet   ShallowConvNet on raw windows
  deepconvnet      DeepConvNet on raw windows
  eegnet           EEGNet on raw windows
  cnnlstm          CNN-LSTM on raw windows

Data: reads from eeg_preprocessed_{N}ch.npz (generated by cache_modma_eeg.py)
All models share the same preprocessed cache. Differences are attributable
exclusively to architecture, not preprocessing.
 
Output per model:
  outputs/results/classical_dl/eeg/{model}_{N}ch.json
  outputs/results/classical_dl/eeg/{model}_{N}ch_curves.json

Usage:
  python src/training/dl_eeg_benchmark.py --model deepconvnet --channels 64 --epochs 100
  python src/training/dl_eeg_benchmark.py --model deepconvnet --channels 19 --epochs 100
"""
